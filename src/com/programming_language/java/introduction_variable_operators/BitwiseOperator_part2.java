package com.programming_language.java.introduction_variable_operators;

public class BitwiseOperator_part2 {

    public static void main(String[] args) {
        int x = 3;
        System.out.println(~x); //Bitwise complement operator

        System.out.println(x<<1); //left shift operator
        System.out.println(x<<2); //left shift operator
        System.out.println(x<<3); //left shift operator




    }
}

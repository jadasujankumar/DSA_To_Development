package com.programming_language.java.introduction_variable_operators;

import java.util.*;

public class RelationalOperators {

    public static void main(String[] args) {
        int a = 4;
        int b = 7;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
